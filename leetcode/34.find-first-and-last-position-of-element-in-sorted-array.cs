// @lc code=start
public class Solution
{
    public int[] SearchRange(int[] nums, int target)
    {
        int left = 0, right = nums.Length - 1;
        int start = -1;
        while (left <= right)
        {
            int mid = (left + right) / 2;
            if (nums[mid] < target)
            {
                left = mid + 1;
            }
            else if (nums[mid] > target)
            {
                right = mid - 1;
            }
            else
            {
                start = mid;
                right = mid - 1;
            }
        }
        if (start == -1) return [-1, -1];
        int end = -1;
        left = start;
        right = nums.Length - 1;
        while (left <= right)
        {
            int mid = (left + right) / 2;
            if (nums[mid] < target)
            {
                left = mid + 1;
            }
            else if (nums[mid] > target)
            {
                right = mid - 1;
            }
            else
            {
                end = mid;
                left = mid + 1;
            }
        }
        return [start, end];
    }
}
